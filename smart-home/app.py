import time
import random
import logging
import sys

# Налаштовуємо логер, щоб він виводив усе в консоль (stdout)
logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(name)s - %(levelname)s - %(message)s', stream=sys.stdout)
logger = logging.getLogger("SmartHome")

logs =[
    (logging.INFO, "Увімкнено світло в коридорі"),
    (logging.INFO, "Увімкнено світло у вітальні"),
    (logging.WARNING, "Датчик вологості не відповідає"),
    (logging.ERROR, "Протікання води у ванній!"),
    (logging.INFO, "Вимкнено світло у вітальні"),
    (logging.INFO, "Вимкнено світло в коридорі"),
]

logger.info("Мікросервіс SmartHome запущено. Починаю моніторинг...")

while True:
    time.sleep(random.randint(2, 5)) # Чекаємо від 2 до 5 секунд
    log_level, message = random.choice(logs)
    logger.log(log_level, message)







































































































































































































    